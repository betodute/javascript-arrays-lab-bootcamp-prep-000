var kittens = ["Milo", "Otis", "Garfield"];

function destructivelyAppendKitten () {
  kittens.push();
}

function destructivelyPrependKitten () {
  kittens.shift();
}

function destructivelyRemoveLastKitten () {
  kittens.pop() ;
}

function destructivelyRemoveFirstKitten () {
  kittens.unshift();
}

